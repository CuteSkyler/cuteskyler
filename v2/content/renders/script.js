function randomise(){
    var ran = Math.floor(Math.random()*renders.length)
    var ran2 = Math.floor(Math.random()*renders.length)
    if(ran >= renders.length-10){
        ran = renders.length-11
    }
    if(ran2 >= renders.length-3){
        ran2 = renders.length-4
    }
    for(let i = 0; i < 3; i++){
        document.getElementById('f'+(i+1)).style.backgroundImage = "url('"+renders[ran2+i]+"')"
        document.getElementById('af'+(i+1)).href = renders[ran2+i]
    }
    for(let i = 0; i < 10; i++){
        document.getElementById((i+1)).style.backgroundImage = "url('"+renders[ran+i]+"')"
        document.getElementById('a'+(i+1)).href = renders[ran+i]
    };
};

randomise()